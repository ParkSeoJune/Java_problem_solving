package codeup;

import java.util.Scanner;
public class number_1125 {
    public static void main(String[] args) {
        Scanner in = new Scanner(System.in);
        int x = in.nextInt();
        System.out.printf("%o ", x);
        System.out.printf("%X", x);
    }
}
